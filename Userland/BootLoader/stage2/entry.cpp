static void putc(unsigned char c, unsigned char forecolour, unsigned char backcolour, int x, int y)
{
     uint16_t attrib = (backcolour << 4) | (forecolour & 0x0F);
     volatile uint16_t * where;
     where = (volatile uint16_t *)0xB8000 + (y * 80 + x) ;
     *where = c | (attrib << 8);
}

static void puts(const char *str) {
    for (size_t i = 0; str[0] != '\0'; ++i) {
        putc(str[i], 4, 0, i, 0);
    }
}

